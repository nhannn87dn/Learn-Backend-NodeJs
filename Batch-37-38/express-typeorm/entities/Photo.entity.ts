import { Column, Entity, ManyToOne, PrimaryGeneratedColumn } from 'typeorm';
import { Product } from './Product.entity';
@Entity({ name: 'Photos' })
export class Photo {
  @PrimaryGeneratedColumn({ name: 'Id' })
  id: number;

  // ----------------------------------------------------------------------------------------------
  // NAME
  // ----------------------------------------------------------------------------------------------
  @Column({ name: 'Name', type: 'nvarchar', length: 255 })
  name: string;

  // ----------------------------------------------------------------------------------------------
  // Url
  // ----------------------------------------------------------------------------------------------
  @Column({ name: 'Url', type: 'varchar', length: 255 })
  url: string;

  // ----------------------------------------------------------------------------------------------
  // Alt
  // ----------------------------------------------------------------------------------------------
  @Column({ name: 'Alt', type: 'nvarchar', length: 255 })
  alt: string;

  // ----------------------------------------------------------------------------------------------
  // RELATIONS
  // ----------------------------------------------------------------------------------------------
  @ManyToOne(() => Product, (p) => p.photos)
  product: Product;

}
